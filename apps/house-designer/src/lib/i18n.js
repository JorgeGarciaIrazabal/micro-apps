const DICTS = { en, es }

export const SUPPORTED_LANGS = ['en', 'es']

// Returns a t(key, vars?) function for the given language.
// Falls back to English for any missing key.
export function createT(lang) {
  const dict = DICTS[lang] || DICTS.en
  return function t(key, vars) {
    const tpl = dict[key] ?? en[key] ?? key
    if (!vars) return tpl
    return tpl.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ''))
  }
}

// Convert a registry category string to its i18n key.
// e.g. 'Stairs & Balcony' → 'cat.stairs_balcony'
export function catKey(category) {
  return 'cat.' + category.toLowerCase().replace(/[^a-z0-9]+/g, '_').replace(/_+$/, '')
}
